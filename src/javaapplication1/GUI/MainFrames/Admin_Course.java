    private void setPanelData(Course x) {
        jTextField6.setText("" + x.getCname());
        jTextField8.setText("" + x.getCreditHours());
        String Available = x.getCourseAvailability();
        if (Available.equals("Available")) {
            buttonGroup3.setSelected(jRadioButton1.getModel(), true);
            buttonGroup3.setSelected(jRadioButton2.getModel(), false);
        } else if (Available.equals("NotAvailable")) {
            buttonGroup3.setSelected(jRadioButton2.getModel(), true);
            buttonGroup3.setSelected(jRadioButton1.getModel(), false);
        }

    }

    private void resetPanel() {
        jTextField8.setText("");
        jTextField6.setText("");
        buttonGroup3.setSelected(jRadioButton1.getModel(), false);
        buttonGroup3.setSelected(jRadioButton2.getModel(), false);
    }
