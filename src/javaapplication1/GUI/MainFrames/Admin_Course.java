    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String ID = "";
        String Name = "";
        int Hours = 0;
        String available = "";
        Course x = new Course();
        if (jTextField9.getText().equals("") || jTextField11.getText().equals("") || jTextField10.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Empty Field(s)",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
        } else if (jTextField9.getText().contains("@") || jTextField9.getText().contains(".") || jTextField11.getText().contains("@") || jTextField11.getText().contains(".")) {
//                
            JOptionPane.showMessageDialog(this, "Dont add @ or . to textFields",
                    "ERROR", JOptionPane.ERROR_MESSAGE);

//                
        } else if (!SignUp_Form.isNumeric(jTextField10.getText())) {
            JOptionPane.showMessageDialog(this, "Cridet Hour must be integer number",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {

            if (jRadioButton3.isSelected()) {
                available = "Available";
            } else if (jRadioButton4.isSelected()) {
                available = "NotAvailable";
            }
            ID = jTextField9.getText();
            Hours = Integer.parseInt(jTextField10.getText());
            Name = jTextField11.getText();

            if (x.setCoursID(ID)) {
                ID = jTextField9.getText();
                adminSetCourseAvailablity y = new AdminCourseAdapter(new Course());

                y.addNewCourse(available, Name, ID, Hours);
            } else if (!x.setCoursID(ID)) {
                JOptionPane.showMessageDialog(this, "ID already taken",
                        "ERROR", JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

